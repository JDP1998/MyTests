package Darts;

public class Player {
	private String name;
	private int number;

	public void set_Name(String name) {
		this.name = name;
	}

	public void set_Number(int number) {
		this.number = number;
	}

	public String get_Name() {
		return name;
	}

	public int get_Number() {
		return number;
	}
}
